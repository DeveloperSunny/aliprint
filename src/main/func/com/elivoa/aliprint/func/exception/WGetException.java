package com.elivoa.aliprint.func.exception;

/**
 * WGetException
 * 
 * @author Bo Gao elivoa[AT]gamil.com, [Dec 22, 2011]
 */
public class WGetException extends Exception {

	private static final long serialVersionUID = -5867043175109913899L;

	public WGetException() {
		super();
	}

	public WGetException(String message, Throwable cause) {
		super(message, cause);
	}

	public WGetException(String message) {
		super(message);
	}

	public WGetException(Throwable cause) {
		super(cause);
	}

}
